#include <stdio.h> 

int main(){
	int a = 4;
	int b = 3;
	
	printf("a = %d\n", a);
	printf("b = %d\n", b);
	
	printf("a > b = %d\n", a > b);
	printf("a < b = %d\n", a < b);
	printf("a >= b = %d\n", a >= b);
	printf("a <= b = %d\n", a <= b);
	printf("a == b = %d\n", a == b);
	printf("a != b = %d\n", a != b);
	
	return 0;
}
